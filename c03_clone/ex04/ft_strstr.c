char	*ft_strstr(char *src, char *to_find)
{
	int	i;

	i = 0;
	while (*src != 0)
	{
		while (*src == to_find[i])
		{
			src++;
			i++;
		}
		src -= i;
		if (to_find[i] == 0)
			return (src);
		i = 0;
		src++;
	}
}
